export const getTranslation = (
  lang: "en" | "es",
  path: string
): string => {
  const keys = path.split(".");
  let value: any = translations[lang];

  for (const key of keys) {
    if (value && typeof value === "object" && key in value) {
      value = value[key];
    } else {
      return path;
    }
  }

  return typeof value === "string" ? value : path;
};
